
class Main
{
  public static void main(String args[])
  {
    System.out.println("Demonstartion of  Data Types");
    Integer in = 10;
    System.out.println("Integer: Value is "+in+". Type is "+in.getClass().getName());
    Float f = 10.5f;
    System.out.println("Float: Value is "+f+". Type is "+f.getClass().getName());
    Double d = 10.5;
    System.out.println("Double: Value is "+d+". Type is "+d.getClass().getName());
    Character c = 'A';
    System.out.println("Character: Value is "+c+". Type is "+c.getClass().getName());
    Boolean b = true;
    System.out.println("Boolean: Value is "+b+". Type is "+b.getClass().getName());
  }
}